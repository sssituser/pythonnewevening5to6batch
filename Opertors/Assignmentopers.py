num1 = int(input('Enter num1 : ')) # num1 = 10
num2 = int(input('Enter num2 : ')) # num2 = 20
print(f'num1 = {num1}')

num1 += num2 # num1 = num1+num2
print(f'num1 = {num1}')
num1 -=num2 # num1 = num1-num2
print(f'num1 = {num1}')
num1*=num2 # num1 = num1 * num2
print(f'num1 = {num1}')
num1 //=num2 # num1 = num1//num2
print(f'num1 = {num1}')
num1 %=num2 # num1 = num1%num2
print(f'num1 = {num1}')
num1 **=num2 # num1 = num1**num2
print(f'num1 = {num1}')


